import streamlit as st

def get_navigator():
    """初心者向け！投資のキホン用語ナビゲーター"""
    with st.expander("📖 わからない言葉があったら見てね！【用語ナビを開く】", expanded=False):
        st.markdown("""
        **大切な言葉の意味をチェックしよう！**
        
        * **📈 インデックス（Index）**  
          「世界中の会社の成績表（せいせきひょう）」のようなもの。みんなでまとまって動くのがとくちょうだよ。
        * **🎁 配当金（はいとうきん）**  
          会社がもうかった時に、持っている人にくれる「おこづかい」のことだよ。
        * **💸 元本割れ（がんぽんわれ）**  
          最初に出したお金よりも、価値が下がっちゃうこと。投資にはこの「リスク」があることを覚えておこう。
        * **⛄ 複利（ふくり）**  
          増えたお金をまた投資して、雪だるま式にどんどん大きくすることだよ！
        """)

def run_financial_unit():
    st.header("🔰 はじめての投資：なにを買えばいいの？")
    
    get_navigator()
    
    st.markdown("投資を始めるとき、一つの会社だけを買うのはドキドキするよね。\\nおすすめなのは、**世界中の会社にまとめて少しずつ投資（とうし）できる「投資信託（とうししんたく）」**だよ。")

    st.info("💡 **ポイント:** ここで紹介するのは、手数料（てすうりょう）が安くて、世界中に投資できる人気のものだよ。")

    col1, col2 = st.columns(2)

    with col1:
        st.subheader("🌍 オルカン (全世界)")
        st.markdown("**（例：eMAXIS Slim 全世界株式）**")
        st.write("これ一つで、アメリカや日本、ヨーロッパなど**世界中の約3,000社にまとめて投資**してくれるよ。")
        st.success("✅ **いいところ:** どこかの国が不景気になっても、ほかでカバーできるから安心だね。")
        st.warning("⚠️ **ちゅうい:** すっごく速く増えるわけじゃないよ。（平均して1年に5〜7%くらい）")

    with col2:
        st.subheader("🇺🇸 S&P500 (アメリカ)")
        st.markdown("**（例：eMAXIS Slim 米国株式）**")
        st.write("アメリカのトップ企業500社（Apple、Amazon、Microsoftなど）にまとめて投資するよ。")
        st.success("✅ **いいところ:** アメリカはこれまでずっと成長してきたから、オルカンより利益が出やすいこともあるよ。")
        st.warning("⚠️ **ちゅうい:** もしアメリカが不景気になると、大きく値下がりすることもあるよ。")

    st.divider()

    st.subheader("🏦 銀行（ぎんこう）にあずけるのと何がちがうの？")
    st.markdown("""
    * **銀行にあずける**: お金は絶対に減らないけど、今の金利だと100万円あずけても1年で数十円〜きゅう百円しか増えないんだ。
    * **投資（とうし）**: 上がったり下がったりするリスクはあるけど、10年、20年と長く続けると、**お金が雪だるま式に増える（複利の力）**チャンスがあるよ。
    """)
    
    st.markdown("👉 **つぎのタブ「📊 市場概況 & 戦略銘柄」で、実際の数字を見てみよう！**")
