    with open(DATA_PATH, "r", encoding="utf-8") as f:
        columns = json.load(f)
    
    updated = 0
    for col in columns:
        col_id = col["id"]
        if col_id in SEO_DATA:
            seo = SEO_DATA[col_id]
            if "definition" in seo:
                col["definition"] = seo["definition"]
            if "faq" in seo:
                col["faq"] = seo["faq"]
            if "sources" in seo:
                col["sources"] = seo["sources"]
            if "related_links" in seo:
                col["related_links"] = seo["related_links"]
