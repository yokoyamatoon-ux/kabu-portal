def main():
    with open(DATA_PATH, "r", encoding="utf-8") as f:
        manga = json.load(f)
    
    updated = 0
    for ep in manga:
        ep_num = ep["ep"]
        if ep_num in MANGA_SEO:
            ep["description_long"] = MANGA_SEO[ep_num]["description_long"]
            updated += 1
    
    with open(DATA_PATH, "w", encoding="utf-8") as f:
        json.dump(manga, f, ensure_ascii=False, indent=4)
    
    print(f"Updated {updated} manga episodes with description_long")

if __name__ == "__main__":
    main()
